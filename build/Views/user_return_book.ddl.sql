CREATE OR REPLACE FORCE EDITIONABLE VIEW "USER_RETURN_BOOK" (
    "EMAIL",
    "U_PASSWORD",
    "U_ROLE",
    "U_NAME",
    "FINE_RATE",
    "TOTAL_DAYS",
    "BOOK_LIMIT",
    "TOTAL_ISSUED"
  ) AS
SELECT "EMAIL",
  "U_PASSWORD",
  "U_ROLE",
  "U_NAME",
  "FINE_RATE",
  "TOTAL_DAYS",
  "BOOK_LIMIT",
  "TOTAL_ISSUED"
FROM STUDENT_ENTITY
UNION
SELECT "EMAIL",
  "U_PASSWORD",
  "U_ROLE",
  "U_NAME",
  "FINE_RATE",
  "TOTAL_DAYS",
  "BOOK_LIMIT",
  "TOTAL_ISSUED"
FROM TEACHER_ENTITY;